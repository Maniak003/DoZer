  /* USER CODE BEGIN 2 */
  //HAL_GPIO_WritePin(GPIOC, GPIO_PIN_15, GPIO_PIN_RESET); // Включаем светодиод.
  HAL_ADCEx_Calibration_Start(&hadc1);
  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  int j = 0;
  uint8_t prefix[3] = {'<', 'B', '>'};
